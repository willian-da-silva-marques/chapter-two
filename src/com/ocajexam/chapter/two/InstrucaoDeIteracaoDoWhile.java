package com.ocajexam.chapter.two;

/**
 * @since 08/01/2020
 * @author willian
 *
 * A instrução de iteração de do-while é usada para iterar sobre um trecho de código.
 * Ela é muito semelhante a instrução de laço while, mas, sempre executa o corpo do laço pelo menos uma vez.
 * A expressão da instrução do-while precisa ser verdadeira para que o corpo do laço seja executado.  
 */
public class InstrucaoDeIteracaoDoWhile {

	public static void main(String[] args) {
		/**
		 * A formação geral da instrução while é:
		 */
		boolean expressao = true;
		do {
			// corpo do laço com sequência de instruções que será executado pelo menos uma vez.
		} while (expressao);
	}

}
