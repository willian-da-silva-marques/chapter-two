/**
 * @since 08/01/2020
 * @author willian
 *
 * A instrução de iteração de laços for é usada para iterar sobre um trecho de código
 * Ela tem partes principais que são: inicialização, expressão e iteração  
 */
public class InstrucaoDeIteracaoFor {

	public static void main(String[] args) {
		/**
		 * A forma geral da instrução condicional for é:
		 */
		for (int i = 1; i < 4; i++) {
			System.out.println(i+"º iteração");
		}
	}
}